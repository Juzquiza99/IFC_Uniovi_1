# Este programa se encarga de transformar una temperatura de 21 grados
# Celsius y transformarla a grados Farenheit
c=21 #Definimos el valor de la variable c

F=9./5*c+32; #realizamos la operacion para transformarlo y lo guardamos
# en la variable F

print("La temperatura en grados Farenheit (F)") 
# Mostramos por pantalla ese texto
print("%.2f" % F) #Mostramos por pantalla el valor F
# con dos decimales (2f)
